print(type(158)) # int -> integer -> Número entero
print(type(74.2)) # float -> Número real
print(type(6j)) # complex -> Número complejo
print(type("Hola")) # str -> string -> Texto
print(type('hola'))
print(type(True)) # bool -> booleano
print(type(False))
print(type([5,"Hola",True])) # list -> Secuencia ordenada y mutable de datos
print(type((5,"Hola",True))) # tuple -> Secuencia ordenada e inmutable de datos
print(type({"hola": [1,2,3]})) # dict -> Secuencia ordenada, nombrada y mutable de datos